<b class = "s">Recovery and ADHD: </b><br><br>
While there is no cure for ADHD, individuals with ADHD can experience mental health recovery. For these 
individuals, recovery can best be understood as the ongoing management of ADHD symptoms. According to the
Substance Abuse and Mental Health Administration (SAMHSA), the two main hallmarks of mental health recovery
are living a “meaningful life” and growing toward one’s “full potential.” For those with ADHD, these goals 
can be attainable. Despite the many challenges they face, resources exist that can help individuals with ADHD 
attain a level of well-being marked by independence, healthy interdependence, 
hope and personal satisfaction.<br><br>


<b class = "s">Treatment Research:</b><br>
The bulk of treatment research on ADHD has focused on the condition in children, and the options for 
them have a strong evidence-base for symptom reduction. For many, the need for intervention persists over
the long-term. Research from the landmark National Institute of Mental Health Multimodal Treatment Study
of ADHD showed significant improvement in behavior at home and school in children with ADHD who received
carefully monitored medication in combination with behavioral treatment. These children also showed
better relationships with their classmates and family than did not children receiving this combination of
treatment. Further research confirms that combining behavioral and stimulant treatments are more effective
 than either treatment alone.<br>
Although ADHD has been less thoroughly researched in adults than in children, adults who have been correctly
 diagnosed with the disorder can still take advantage of whatever treatments best meet their needs. Working 
 with one or several health and mental health care practitioners, adults with ADHD can learn to manage 
 symptoms as they are expressed in their lives.
ADHD is a condition that affects individuals “across the lifespan.” This means that ADHD symptoms are
 usually experienced from one phase of life to the next, and that they extend to the various spheres of
  life during any particular life phase.
